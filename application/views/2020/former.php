              <h2 class="title"><a href="#">Our History</a></h2>
              <p>
                  107网站工作室，初成立于2003年12月，在现哲学院党委书记马润成老师的指导下，由河南大学2002级学生李少勇创立，与党总支办公室一起在老校区计算机楼1楼工作。2004年9月学院搬迁至新区时，学院拨出107室并按当时的科技公司模式装修作为办公室，学院和校团委服务器都迁于此。受当时网络上工作室的启发，但一时之间没有想到好的名称，因此所做网站技术支持都署名107网站工作室。全校近1/4的网站出自107。107网站工作室名声流传开来。当时由于房间较小，只能容纳5-6名同学， 所以最初工作室每年只招两人。到后来由于我工作室对学院做出的突出贡献，受到了院领导以及各位老师的大力支持，由107办公室 搬至116办公室，实验室规模扩大，于2014年开始扩招。
              </p>
