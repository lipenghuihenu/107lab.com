              <h2 class="title"><a href="#">Our Team</a></h2>
              <p>
                  我们是一群具有专业技术的在校大学生组成的团队，是一个富有想象力和创造力的团队，每年我们都会吸纳一批优秀的同学加入到我们 的团队中，以专业的技术、独特的创意和青春的热情为工作室注入源源不断的活力。在这里，你可以结识到各个专业，各个年级的朋友。 我们不只是一个团队，更像是一个大家庭。每年都会组织一些有趣的活动，一起聚餐，一起唱歌，一起旅行，让你的大学生活更加有意义。 在这里每个人都是精英。 目前实验室成员分布学院的各个专业，各个班级，主力为大二大三。
              </p>
